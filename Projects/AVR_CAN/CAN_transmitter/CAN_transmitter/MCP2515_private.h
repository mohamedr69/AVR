#define MCP2515_RXF3SIDH	0x10
#define MCP2515_RXM0SIDH	0x20
#define MCP2515_RXB0CTRL	0x60
#define MCP2515_RXB1CTRL	0x70
#define MCP2515_RXM1SIDH	0x24

#define MCP2515_CANINTE		0x2B
